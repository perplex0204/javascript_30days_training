// 單雙引號無差異
var str = '字串測試';
var str2 = "字串測試2";

// 若要在單引號內包覆雙引號，或是相反

var str3 = '像這樣 let"s go!';
var str4 = "或這樣 let's go!";
var str5 = '或這樣 let\'s go!';

// 多組字串的相加

var hello_world = 'Hello, ' + 'World';

// 多行字串，透過\來繼續
// \後不能有任何東西

var hello = '第一行\
第二行\
第三行';